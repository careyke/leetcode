/**
 * @param {number} n
 * @return {number}
 * 推导公式的原理：就是当某个节点为根节点时，其他节点的摆放位置有多少种
 * dp[n] = sum(dp[n-i]*dp[i-1])
 */
var numTrees = function (n) {
  const dp = new Array(n + 1).fill(0);
  dp[0] = 1;
  dp[1] = 1;
  dp[2] = 2;

  for (let i = 3; i <= n; i++) {
    for (let j = 0; j < i; j++) {
      dp[i] += dp[j] * dp[i - j - 1];
    }
  }

  return dp[n];
};
